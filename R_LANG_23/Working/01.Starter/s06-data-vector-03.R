# Vector 자료구조 

# 참조 

v <- c(1,3,5,7)
v[1] #1
v[2] #3
v[3] #5
v[4] #7 

#길이 
vl=length(v) # 4 
v[1:vl]
v[2:3] # 3,5 

# 상대참조 
rc <- c(2:4)
rc # 2 3 4

v[rc] # 3 5 7
v[c(2:4)] # 3 5 7


